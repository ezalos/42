#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*ptr;
	t_list	*tmp;

	ptr = *alst;
	while (ptr->next != NULL)
	{
		tmp = ptr->next;
		ft_lstdelone(&tmp, del);
		ptr = ptr->next;
	}
	ft_lstdelone(&tmp, del);
}
